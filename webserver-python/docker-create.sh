#!/bin/bash

#################################################################################
# Defining variables
#################################################################################
imagename="webserverpython:1.0"
containername="webserverpython"

#################################################################################
# Building image
#################################################################################
docker image build -t ${imagename} .

#################################################################################
# Running container
#################################################################################
docker container run -d \
                     --restart always \
                     -p 8080:8000 \
                     -v $(pwd)/webserver:/app \
                     --name ${containername} \
                     ${imagename}

